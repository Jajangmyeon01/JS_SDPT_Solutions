let difficulty = 2;

switch(difficulty){
    case 1:
        alert("Easy");
        break;
    case 2:
        alert("Normal");
        break;
    case 3: 
        alert("Hard");
        break;
    default:
        alert("Invalid Difficulty");
        break;
}


let letter = "a";

switch(letter){
    case "a":
    case "A":
        alert("Letter a/A");
        break;
    case "b":
    case "B":
        alert("Letter b/B");
        break;
    case "c": 
    case "C":
        alert("Letter c/C");
        break;
    default:
        alert("Invalid Letter");
        break;
}